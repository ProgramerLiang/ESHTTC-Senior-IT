'''
生物社团到植物园参加活动，帮忙给小树苗搭支架，为了方便管理，每棵苗都是按顺序编号的，现在需要每隔一棵苗（也就是给奇数编号的树苗），也就是给奇数编号的树苗安装一个支架，小明注意到，他负责的这排，编号是从a到b，请你帮小明编写一个程序，计算小明要装多少个支架？（要求用循环结构解决该问题）。
【样例】
树苗开始编号是：11
树苗结束编号是：20
小明要安装的支架是：5个
'''
ans = 0
a = int(input("树苗开始编号是："))
b = int(input("树苗结束编号是："))
for i in range(a, b+1):
    if i%2 == 1:
        ans += 1
print("小明要安装的支架是：", ans, "个")
